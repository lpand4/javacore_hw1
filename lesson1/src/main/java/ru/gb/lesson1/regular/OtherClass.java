package ru.gb.lesson1.regular;


/**
 * Другой, очень полезный класс приложения. Здесь мы можем описать
 * его основные назначения и способы взаимодействия с ним.
 */
public class OtherClass {

    /**
     * Функция сумирования двух целых чисел.
     *
     * @param a первое слогаемое
     * @param b второе слогаемое
     * @return сумма а и б
     */
    public static int add(int a, int b) {
        return a + b;
    }

    /**
     * Функция деления одного числа на второе.
     *
     * @param a делимое
     * @param b делитель
     * @return результат деления
     */
    public static int div(int a, int b) {
        return a / b;
    }

    /**
     * Функция умножения двух чисел
     * @param a первое число
     * @param b второе число
     * @return результат умножения
     */
    public static int mul(int a, int b) {
        return a * b;
    }

    /**
     * Функция вычитания из одного числа другого
     * @param a уменьшаемое
     * @param b вычитаемое
     * @return результат вычитания
     */
    public static int sub(int a, int b) {
        return a - b;
    }
}
